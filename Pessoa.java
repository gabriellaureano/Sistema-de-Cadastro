import java.util.ArrayList;

public class Pessoa {
    String nome;
    int idade;
    int plano;

    public Pessoa(String nome, int idade, int plano){
        this.nome = nome;
        this.idade = idade;
        this.plano = plano;
    }

    public String getNome() {
        return nome;
    }

    public int getIdade() {
        return idade;
    }

    public int getPlano() {
        return plano;
    }




}
